// Classe Carro representa um modelo de carro
public class Carro {
    // Atributos (características) do carro
    String marca;
    String modelo;
    String cor;

    // Método para ligar o carro (ação)
    void ligar() {
        System.out.println("O carro está ligado.");
    }

    // Método para acelerar o carro (ação)
    void acelerar() {
        System.out.println("O carro está acelerando.");
    }
}
